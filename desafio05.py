# Requesting inputs
num1 = float(input('Digite um número real: '))
num2 = int(input('Digite um número natural: '))

# Performing and printing calculations
print(f'Soma: {num1} + {num2} = {num1 + num2}')
print(f'Subtração: {num1} - {num2} = {num1 - num2}')
print(f'Multiplicação: {num1} x {num2} = {num1 * num2}')
print(f'Divisão: {num1} / {num2} = {num1 / num2}')
print(f'Exponenciação: {num1} ^ {num2} = {num1 ** num2}')
